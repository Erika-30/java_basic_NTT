package com.ntt.tgp;

public class OperacionesAritmeticas {
    public static void main(String[] args) {
        // Declaración de variables para los operandos
        int a = 10;
        int b = 3;

        System.out.println("Operaciones Aritméticas con los números " + a + " y " + b);

        // Operación de Suma
        // Paso 1: Declarar la variable para el resultado de la suma
        int suma;
        // Paso 2: Inicializar la variable con la suma de 'a' y 'b'
        suma = a + b;
        // Paso 3: Usar la variable para imprimir el resultado
        System.out.println("Suma: " + suma);  // Imprime: Suma: 13

        // Operación de Resta
        // Paso 1: Declarar la variable para el resultado de la resta
        int resta;
        // Paso 2: Inicializar la variable con la resta de 'a' y 'b'
        resta = a - b;
        // Paso 3: Usar la variable para imprimir el resultado
        System.out.println("Resta: " + resta);  // Imprime: Resta: 7

        // Operación de Multiplicación
        // Paso 1: Declarar la variable para el resultado de la multiplicación
        int multiplicacion;
        // Paso 2: Inicializar la variable con la multiplicación de 'a' y 'b'
        multiplicacion = a * b;
        // Paso 3: Usar la variable para imprimir el resultado
        System.out.println("Multiplicación: " + multiplicacion);  // Imprime: Multiplicación: 30

        // Operación de División
        // Paso 1: Declarar la variable para el resultado de la división
        int division;
        // Paso 2: Inicializar la variable con la división de 'a' y 'b'
        division = a / b;
        // Paso 3: Usar la variable para imprimir el resultado
        System.out.println("División: " + division);  // Imprime: División: 3

        // Operación de Resto (Módulo)
        // Paso 1: Declarar la variable para el resultado del resto
        int resto;
        // Paso 2: Inicializar la variable con el resto de 'a' y 'b'
        resto = a % b;
        // Paso 3: Usar la variable para imprimir el resultado
        System.out.println("Resto: " + resto);  // Imprime: Resto: 1


    }
}